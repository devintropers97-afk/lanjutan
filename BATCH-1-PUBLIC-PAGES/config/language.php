/**
 * Function untuk get teks berdasarkan bahasa aktif
 * Cara pakai: t('nav_home')
 */
function t($key) {
    global $lang;

    // Ambil bahasa dari session, default ke Bahasa Indonesia
    $current_lang = isset($_SESSION['lang']) ? $_SESSION['lang'] : 'id';

    // Return teks sesuai bahasa
    return isset($lang[$current_lang][$key]) ? $lang[$current_lang][$key] : $key;
}

/**
 * Function untuk switch bahasa
 * Cara pakai: set_language('en')
 */
function set_language($language) {
    $_SESSION['lang'] = $language;
}

/**
 * Function untuk get bahasa aktif
 * Cara pakai: get_language()
 */
function get_language() {
    return isset($_SESSION['lang']) ? $_SESSION['lang'] : 'id';
}
